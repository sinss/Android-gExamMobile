package com.perfectidea.exam.model;

public class GradeComment {
	private String gradeComment;
	private String gradeId;
	private Number rangeFrom;
	private Number rangeTo;
	public String getGradeComment() {
		return gradeComment;
	}
	public void setGradeComment(String gradeComment) {
		this.gradeComment = gradeComment;
	}
	public String getGradeId() {
		return gradeId;
	}
	public void setGradeId(String gradeId) {
		this.gradeId = gradeId;
	}
	public Number getRangeFrom() {
		return rangeFrom;
	}
	public void setRangeFrom(Number rangeFrom) {
		this.rangeFrom = rangeFrom;
	}
	public Number getRangeTo() {
		return rangeTo;
	}
	public void setRangeTo(Number rangeTo) {
		this.rangeTo = rangeTo;
	}

}
